package idlogistic_client.json;

public class JsonMotorista {

	private String codTMS;
	private String cpf;
	private String cnh;
	private String nome;
	
	public String getCodTMS() {
		return codTMS;
	}
	public void setCodTMS(String codTMS) {
		this.codTMS = codTMS;
	}
	public String getCpf() {
		return cpf;
	}
	public void setCpf(String cpf) {
		this.cpf = cpf;
	}
	public String getCnh() {
		return cnh;
	}
	public void setCnh(String cnh) {
		this.cnh = cnh;
	}
	public String getNome() {
		return nome;
	}
	public void setNome(String nome) {
		this.nome = nome;
	} 
	
}
